    private void validaBusqueda() {
        if(isDatosValidosBusqueda()){
            buscarConvenio(txfBusqueda.getText().trim().toUpperCase());
        }else{
            Mensajes.msjParametroVacio();
        }        
    }

    private void estadoTextFields(boolean e) {
        this.txfDescripcion.setEnabled(e);
        chbxEsConvenio.setEnabled(e);
        chbxEstado.setEnabled(e);
    }

    private void personalizaVistaTabla() {
        Utilitarios.formateaColumna(0, tblResultados, 60);
        Utilitarios.formateaColumna(2, tblResultados, 80);
        Utilitarios.formateaColumna(0, tblBeneficio, 60);
        Utilitarios.formateaColumna(2, tblBeneficio, 80);
    }

    private void obtenerDatosTabla() throws SecurityException {
        resetComponent();
        beanTabla = (SeguroVida) modeloSeguroVida.get(tblResultados.getSelectedRow());
        this.txfDescripcion.setText(beanTabla.getDescripcion());
        chbxEsConvenio.setSelected(beanTabla.getEsconvenio() == 1?true:false);
        chbxEstado.setSelected(beanTabla.getEstado());
        listarBeneficios(beanTabla.getIdSeguroVida());
    }

    private void resetComponent() {
        this.txfDescripcion.setText("");
        chbxEsConvenio.setSelected(false);
        jsCubierto.setValue(0);
        chbxEstado.setSelected(false);
        Utilitarios.resetPaintBorderTxf(txfDescripcion);
    }

    private void activarBotones(boolean nuevo, boolean modificar, boolean guardar, boolean cancelar) {
        btnNuevo.setEnabled(nuevo);
        btnEditar.setEnabled(modificar);
        btnGuardar.setEnabled(guardar);
        btnCancelar.setEnabled(cancelar);
    }

    private void resetPaintComponent() {
        Utilitarios.resetPaintTxf(txfDescripcion);
    }

    private void configuraNuevo() {
        banBoton = 1;
        tblResultados.setEnabled(true);
        inicializar();
        resetPaintComponent();
        estadoTextFields(true);
        activarBotones(false, false, true, true);
        personalizaVistaTabla();
    }

    private void configuraGuardar() {
        if (banBoton == 1) {
            if (isDatosValidos()) {
                registrar();
                resetComponent();
                estadoTextFields(false);
                resetPaintComponent();
                buscarConvenio(oSeguroVida.getDescripcion());
            } else {
                JOptionPane.showMessageDialog(null, "Por favor ingrese todos los datos", "ATENCION", JOptionPane.INFORMATION_MESSAGE);
                paintComponent();
            }

        } else if (banBoton == 2) {
            if (isDatosValidos()) {
                actualizar();
                //buscar por razon social y listar en la tabla
                buscarxDescripcion(txfDescripcion.getText().toUpperCase().trim());
                //resetear componentes
                resetComponent();
                estadoTextFields(false);
                resetPaintComponent();
                //buscarPersonaJuridica();
            } else {
                JOptionPane.showMessageDialog(null, "Por favor ingrese todos los datos", "ATENCION", JOptionPane.INFORMATION_MESSAGE);
                paintComponent();
            }
        }
    }

    private boolean isDatosValidos() {
        return (!this.txfDescripcion.getText().trim().equals(""));
    }

    private void registrar() {
        oSeguroVida = registrarConvenio();
        if (oSeguroVida != null) {
            Mensajes.msjRegCorrecta();
        } else {
            Mensajes.msjRegError();
        }
    }

    private SeguroVida registrarConvenio() {
        oSeguroVida = new SeguroVida();
        oSeguroBl = new SeguroBl();

        oSeguroVida.setDescripcion(txfDescripcion.getText().toUpperCase().trim());
        oSeguroVida.setEstado(chbxEstado.isSelected());
        oSeguroVida.setFechaReg(new Date());
        oSeguroVida.setEsconvenio(chbxEsConvenio.isSelected()?1:0);

        return oSeguroBl.registrarPersona(oSeguroVida);
    }

    private void paintComponent() {
        Utilitarios.paintBorderTxfVacio(txfDescripcion);
    }

    private void actualizar() {
        //actualizarPresentacion();
        int res = actualizarDescripcion();
        if (res == 0) {
            Mensajes.msjActErronea();
        } else {
            Mensajes.msjActCorrecta();
        }
        personalizaVistaTabla();
    }

    private int actualizarDescripcion() {
        oSeguroVida = new SeguroVida();
        oSeguroBl = new SeguroBl();

        oSeguroVida.setIdSeguroVida(beanTabla.getIdSeguroVida());
        oSeguroVida.setDescripcion(txfDescripcion.getText().trim().toUpperCase());
        oSeguroVida.setEstado(chbxEstado.isSelected());
        oSeguroVida.setEsconvenio(chbxEsConvenio.isSelected()?1:0);
        return oSeguroBl.actualizar(oSeguroVida);

    }

    private void buscarxDescripcion(String descripcion) {
        oSeguroBl = new SeguroBl();
        listSeguroVida = oSeguroBl.buscarF(descripcion);
        if (!listSeguroVida.isEmpty()) {
            modeloSeguroVida.addAll(listSeguroVida);
        } else {
            Mensajes.msjResultVacio();
        }
        personalizaVistaTabla();
    }

    private void configuraActualizar() {
        banBoton = 2;
        estadoTextFields(true);
        activarBotones(true, false, true, true);
    }

    private void configuraCancelar() {
        inicializar();
        oSeguroVida = null;
    }

    void inicializar() {
        oModeloBeneficio.clear();
        modeloSeguroVida.clear();
        personalizaVistaTabla();
        txfBusqueda.setText("");
        txfDescripcion.setText("");
        chbxEsConvenio.setSelected(false);
        chbxEstado.setSelected(false);
        
        txfbeneficio.setText("");
        jsCubierto.setValue(0);
        chbxEstadoBeneficio.setSelected(false);
        
        estadoTextFields(false);
        activarBotones(true, false, false, true);
    }

    private void registrarBeneficio() {
        if(isDatosValidosBeneficio()){
            oBeneficio = new Beneficio();
            oBeneficio.setSeguroVida(beanTabla);
            oBeneficio.setNombreBeneficio(txfbeneficio.getText().trim().toUpperCase());
            oBeneficio.setPorcCubierto(Integer.parseInt(jsCubierto.getValue().toString()));
            oBeneficio.setEstado(chbxEstadoBeneficio.isSelected());
            oBeneficio = oBeneficioBl.registrar(oBeneficio);
            if(oBeneficio != null){
                resetComponenBeneficio();
                buscarConvenio(beanTabla.getDescripcion());
                //listar los beneficios actuales
                listarBeneficios(beanTabla.getIdSeguroVida());
            }
        }else{
            Mensajes.msjValidarIngreso();
        }
    }

    private boolean isDatosValidosBeneficio() {
        return (!txfbeneficio.getText().trim().equals("")
                && jsCubierto.getValue() != null
                && chbxEstadoBeneficio.isSelected()
                && tblResultados.getRowSelectionAllowed());
    }

    private boolean isDatosValidosBusqueda() {
        return (!txfBusqueda.getText().trim().equals(""));
    }

    private void buscarConvenio(String ref) {
        oSeguroBl = new SeguroBl();
        modeloSeguroVida.addAll(oSeguroBl.buscarF(ref));
        personalizaVistaTabla();
    }

    private void listarBeneficios(int idSeguroVida) {
        oBeneficioBl = new BeneficioBl();
        oModeloBeneficio.clear();
        listBeneficios = oBeneficioBl.buscarXIdSeguro(idSeguroVida);
        for (Beneficio obj : listBeneficios) {
            oModeloBeneficio.add(obj);
        }
    }

    private void resetComponenBeneficio() {
        txfbeneficio.setText("");
        jsCubierto.setValue(0);
        chbxEstadoBeneficio.setSelected(false);
    }
